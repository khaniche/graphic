#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*res;
	size_t	i;

	i = 0;
	res = (char *)s;
	if (n == 0)
		return ;
	else
	{
		while (n > i)
		{
			res[i] = 0;
			i++;
		}
	}
}
