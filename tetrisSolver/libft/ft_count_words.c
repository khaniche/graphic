#include "libft.h"

int	ft_count_words(const char *s, char c)
{
	int i;
	int word;

	word = 0;
	i = 0;
	while (s[i])
	{
		if (s[i] == c && (s[i + 1] != c && s[i + 1] != '\0'))
			word++;
		i++;
	}
	if (s[0] != c)
		word++;
	return (word);
}
