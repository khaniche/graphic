#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char	*res;
	int		i;

	i = 0;
	res = dst;
	while (0 < len && *src != '\0')
	{
		*dst++ = *src++;
		len--;
	}
	while (len > 0)
	{
		*dst++ = '\0';
		len--;
	}
	return (res);
}
